    int validar_mail(){
     
        String mail = txt_mail.getText();
        
        String sql= "SELECT COUNT(*) as total FROM tb_ciclista WHERE mail = ?;";
        try{

            PreparedStatement prep_statement;
            prep_statement = connection.prepareStatement(sql);
            
            prep_statement.setString(1, mail);
                     
            ResultSet rs = prep_statement.executeQuery();
            rs.next();
            return rs.getInt("total");
            
        }catch(SQLException e){
            System.out.println(e);
        }
        
        System.out.println("Error");
        return -1;
    }
