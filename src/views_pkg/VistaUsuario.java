    public void show_ciclistas(){
        String sql = "SELECt * FROM tb_ciclista WHERE 1;";
        
        try{
            
            PreparedStatement prep_statement;
            prep_statement = connection.prepareStatement(sql);
            prep_statement.setQueryTimeout(10);
                        
            ResultSet rs = prep_statement.executeQuery();
            
            Object[]ciclista = new Object[4];
            
            model = (DefaultTableModel)tb_usuarios.getModel();
            
            while(rs.next()){
                ciclista[0] = rs.getInt("id");
                ciclista[1] = rs.getString("name");
                ciclista[2] = rs.getString("mail");
               ciclista[3] = rs.getString("phone");
                model.addRow(ciclista);
            }
            
           tb_usuarios.setModel(model);
            
        }catch(SQLException e){
            System.out.println(e);
        }
    }
     void add_ciclista(){
    
        String nombre = txt_name.getText();
        String correo = txt_mail.getText();
        String contraseña = txt_password.getText();
        String telefono = txt_phone.getText();
        
        String sql= "INSERT INTO tb_ciclista (name, mail, password, phone) VALUES (?,?,?,?);";
        
        try{

            PreparedStatement prep_statement;
            prep_statement = connection.prepareStatement(sql);
            prep_statement.setQueryTimeout(10);
            
            prep_statement.setString(1, nombre);
            prep_statement.setString(2, correo);
            prep_statement.setString(3, contraseña);
            prep_statement.setString(4, telefono);
            
            prep_statement.executeUpdate();
            
        }catch(SQLException e){
            System.out.println(e);
        }
        
        System.out.println("AÑADIDO");
        JOptionPane.showMessageDialog(this, "Registro exitoso");
        this.show_ciclistas();
    }
    
